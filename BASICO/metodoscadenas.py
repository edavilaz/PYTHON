# MÉTODOS PARA CADENAS

a = "Curso de Python"
print(a.lower()) # convierte en minúsculas
print(a.upper()) # convierte en mayúsculas
print(a.swapcase()) # convierte minúsculas a mayúsculas y viceversa
print(a.count("o")) # cuenta lo que se ponga como parámetro da: 2
print(a.replace("Python","java",)) # reemplaza una palabra o frase por otra
print(a.capitalize()) # deja con letra capital(mayúscula inicial)
print(a.find("de")) # enumera los caracteres hasta llegar al parametro ingresado sale. 6
